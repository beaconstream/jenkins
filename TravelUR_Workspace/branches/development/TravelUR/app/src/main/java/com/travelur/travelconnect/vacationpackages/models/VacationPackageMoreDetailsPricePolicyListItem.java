package com.travelur.travelconnect.vacationpackages.models;

/**
 * @author by Abhijit.
 */

public class VacationPackageMoreDetailsPricePolicyListItem {
    String price_includes;
    String price_excludes;

    public VacationPackageMoreDetailsPricePolicyListItem(String price_includes, String price_excludes) {
        this.price_includes = price_includes;
        this.price_excludes = price_excludes;
    }

    public String getPrice_includes() {
        return price_includes;
    }

    public void setPrice_includes(String price_includes) {
        this.price_includes = price_includes;
    }

    public String getPrice_excludes() {
        return price_excludes;
    }

    public void setPrice_excludes(String price_excludes) {
        this.price_excludes = price_excludes;
    }
}
